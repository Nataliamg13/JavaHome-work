package class6;

public class HomeWork6 {
    public static void main(String[] args) {
        // Print even numbers from 20 to 1 (2 ways)
        int number = 20;
        while (number > 0) {
            if (number % 2 == 0) {
                System.out.println(number + "");
            }
            number--;
        }
    }
}