package class7;

public class ForLoops2 {
    public static void main(String[] args) {   ///When you know exactly how many times you want to loop through a
                                          // block of code, use the for loop instead of a while loop:

        int number = 0;
        while (number < 10) {
            System.out.println(number);
            number ++;

        }
    }
}